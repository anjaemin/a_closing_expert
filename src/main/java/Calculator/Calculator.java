package Calculator;

public class Calculator {

    static int mul(int num1, int num2) {
        return num1 * num2;
    }

    static int plus(int num1, int num2) {
        return num1 + num2;
    }

    static int minus(int num1, int num2) {
        return num1 - num2;
    }

    static int divide(int num1, int num2) {
        return num1 / num2;
    }


}
